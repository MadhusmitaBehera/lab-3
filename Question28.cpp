#include <iostream>

using namespace std;

int main() {
           int counter,n,sum;
           cout<<"\n Enter a positive number.";
           cin>>n;
           for(counter=1;counter<=n;counter++)
           {
           	  if(counter%2==0);
           	  sum=sum+counter;
           }
           cout<<"\n Sum of all even numbers:"<<sum<<endl;
           
           
           
           	  
                    
    

    return 0;
}
